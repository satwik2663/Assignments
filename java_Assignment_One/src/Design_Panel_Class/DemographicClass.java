package Design_Panel_Class;

public class DemographicClass 
{
    //Instance Variables
    private String aFirstName;
    private String aLastName;
    private String aPhoneNumber;
    private String aDateOfBirth;
    private String aHeight;
    private String aWeight;
    private String aSsn;
    
    //Getter and Setter for the Instance Variables
    public String getaFirstName() {
        return aFirstName;
    }

    public void setaFirstName(String aFirstName) {
        this.aFirstName = aFirstName;
    }

    public String getaLastName() {
        return aLastName;
    }

    public void setaLastName(String aLastName) {
        this.aLastName = aLastName;
    }

    public String getaPhoneNumber() {
        return aPhoneNumber;
    }

    public void setaPhoneNumber(String aPhoneNumber) {
        this.aPhoneNumber = aPhoneNumber;
    }

    public String getaDateOfBirth() {
        return aDateOfBirth;
    }

    public void setaDateOfBirth(String aDateOfBirth) {
        this.aDateOfBirth = aDateOfBirth;
    }

    public String getaHeight() {
        return aHeight;
    }

    public void setaHeight(String aHeight) {
        this.aHeight = aHeight;
    }

    public String getaWeight() {
        return aWeight;
    }

    public void setaWeight(String aWeight) {
        this.aWeight = aWeight;
    }

    public String getaSsn() {
        return aSsn;
    }

    public void setaSsn(String aSsn) {
        this.aSsn = aSsn;
    }
  
}
